int	ft_strcmp(char *s1, char *s2)
{
	unsigned int	i;

	i = 0;
	while (s1[i] && s2[i])
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		i++;
	}
	if (!s1[i])
		return ('\0' - s2[i]);
	else if (!s2[i])
		return (s1[i] - '\0');
	return (0);
}
