int	ft_is_prime(int nb)
{
	int	i;
	int	max;

	i = 7;
	if (nb <= 1)
		return (0);
	else if (nb == 2 || nb == 3 || nb == 5 || nb == 7)
		return (1);
	else if (nb % 2 == 0 || nb % 3 == 0 || nb % 5 == 0 || nb % 7 == 0)
		return (0);
	max = (nb / 2) + 1;
	while (i < max)
	{
		if (nb % i == 0)
			return (0);
		i += 2;
	}
	return (1);
}
