#include <stdio.h>

int	ft_count_if(char **tab, int length, int (*f)(char *));

int	ft_isdigit(char *str)
{
	while (*str)
	{
		if (*str < 48 || *str > 57)
			return (0);
		str++;
	}
	return (1);
}

int	main(int argc, char **argv)
{
	printf("%d", ft_count_if(argv, argc, &ft_isdigit));
}
