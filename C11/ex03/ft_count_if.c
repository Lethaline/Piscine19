int	ft_count_if(char **tab, int length, int (*f)(char *))
{
	int	i;
	int	cmpt;

	i = 0;
	cmpt = 0;
	while (i < length)
	{
		if (f(tab[i]) != 0)
			cmpt++;
		i++;
	}
	return (cmpt);
}
